<?php

# Ejemplo #1 
// par e impar usando operador ternario.
$numero = 7;

// Operador ternario para determinar si es par o impar
$resultado = ($numero % 2 == 0) ? "Par" : "Impar";

// Mostrar el resultado
echo "El número $numero es: $resultado";



# Ejemplo #2
// par e impar usando if normales
$numero = 7;

// Usando un if para determinar si es par o impar
if ($numero % 2 == 0) {
    $resultado = "Par";
} else {
    $resultado = "Impar";
}

// Mostrar el resultado
echo "El número $numero es: $resultado";



?>
